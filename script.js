// -------- HISTORY POPUP --------
const historyPopup = document.getElementById('popup1');
const historyBtn = document.getElementById('history');
const closeHistory = document.getElementById('button1');

historyBtn.onclick = () => {
    historyPopup.classList.add('show');
    mainContent.classList.add('hide');
    navHead.classList.add('hide');
};

closeHistory.onclick = () => {
    historyPopup.classList.remove('show');
    mainContent.classList.remove('hide');
    navHead.classList.remove('hide');
};

